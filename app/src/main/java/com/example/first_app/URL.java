package com.example.first_app;

public class URL {
    private String name;
    private String url;

    // Constructs a URL object with specified name and age
    public URL(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

}
